public class QuerySchoolBean {
    private int schoolIcon;
    private String schoolName;
    private String[] schoolMark;
    private String schoolType;
    private String schoolLevel;
    private String schoolAddress;

    public int getSchoolIcon() {
        return schoolIcon;
    }

    public void setSchoolIcon(int schoolIcon) {
        this.schoolIcon = schoolIcon;
    }

    public String getSchoolName() {
        return schoolName;
    }

    public void setSchoolName(String schoolName) {
        this.schoolName = schoolName;
    }

    public String[] getSchoolMark() {
        return schoolMark;
    }

    public void setSchoolMark(String[] schoolMark) {
        this.schoolMark = schoolMark;
    }

    public String getSchoolType() {
        return schoolType;
    }

    public void setSchoolType(String schoolType) {
        this.schoolType = schoolType;
    }

    public String getSchoolLevel() {
        return schoolLevel;
    }

    public void setSchoolLevel(String schoolLevel) {
        this.schoolLevel = schoolLevel;
    }

    public String getSchoolAddress() {
        return schoolAddress;
    }

    public void setSchoolAddress(String schoolAddress) {
        this.schoolAddress = schoolAddress;
    }
}
